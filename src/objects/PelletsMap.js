import PelletType from "../enums/PelletType";

export default `
............................
.++++++++++++..++++++++++++.
.+....+.....+..+.....+....+.
.@....+.....+..+.....+....@.
.+....+.....+..+.....+....+.
.++++++++++++++++++++++++++.
.+....+..+........+..+....+.
.+....+..+........+..+....+.
.++++++..++++..++++..++++++.
......+..............+......
......+..............+......
......+..............+......
......+..............+......
......+..............+......
......+..............+......
......+..............+......
......+..............+......
......+..............+......
......+..............+......
......+..............+......
.++++++++++++..++++++++++++.
.+....+.....+..+.....+....+.
.+....+.....+..+.....+....+.
.@++..+++++++..+++++++..++@.
...+..+..+........+..+..+...
...+..+..+........+..+..+...
.++++++..++++..++++..++++++.
.+..........+..+..........+.
.+..........+..+..........+.
.++++++++++++++++++++++++++.
............................
`.split('\n')
    .filter(line => line.length > 0)
    .map(line => {
        let parsedLine = []
        for (let char = 0; char < line.length; char++) {
            switch (line[char]) {
                case '.':
                    parsedLine.push(PelletType.none)
                    break
                case '+':
                    parsedLine.push(PelletType.normal)
                    break
                case '@':
                    parsedLine.push(PelletType.powerUp)
                    break
            }
        }
        return parsedLine
    })